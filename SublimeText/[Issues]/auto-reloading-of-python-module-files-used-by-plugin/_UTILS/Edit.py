class EditStep ():

	def __init__ ( self, cmd, *args ):
		self.cmd  = cmd
		self.args = args

	def run ( self, view, edit ):

		funcs = \
			{
				'insert':  view.insert,
				'erase':   view.erase,
				'replace': view.replace,
			}

		func = funcs.get ( self.cmd )

		if func:
			func ( edit, *self.args )

