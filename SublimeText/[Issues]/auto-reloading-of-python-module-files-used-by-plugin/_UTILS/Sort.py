def get_NaturalSort_Key ( text ):

	if V.rx_Active_SortPattern == V.rx_StartsWithLetter \
	or V.rx_Active_SortPattern == V.rx_StartsWithNumber:
		trimmedText, trimmedText_Position = Utilities.get_AlphaNumeric_StartData_FromText ( text )
		text = trimmedText
	else:
		searchResult = re.search( V.rx_Active_SortPattern, text )
		text         = searchResult.group ( 0 )

	text       = text.strip()
	subStrings = []

	for subString in re.split ( V.rx_WordSegments, text ):

		if subString == "":
			continue

		if subString.isdigit ():
			subStrings.append ( subString.zfill ( V.numberSort_Depth ) )
		elif V.sortOrder_IgnoreCase_Enabled == True:
			subStrings.append ( subString.lower() )
		elif V.sortOrder_IgnoreCase_Enabled == False:
			subStrings.append ( subString )

	return ( subStrings )
