def get_LineIndentation ( view, text ):

	indentationLevel = get_Level ( view, text )
	indentation      = get_IndentationString ( view, indentationLevel )

	return ( indentation )

def get_IndentationString ( view, indentationLevel ):

	indentationCharacter, indentationSize = get_Settings ( view )
	indentation = indentationCharacter * indentationLevel

	return ( indentation )

def get_Settings ( view ):

	indentationSize     = view.settings ().get ( "tab_size" )
	indentation_IsSpace = view.settings ().get ( "translate_tabs_to_spaces" )

	if indentation_IsSpace == True:
		indentationCharacter = SPACE_CHARACTER * indentationSize

	elif indentation_IsSpace == False:
		indentationCharacter = TAB_CHARACTER

	return ( indentationCharacter, indentationSize )

def get_Level ( view, text, roundUp_Enabled = False ):

	indentationCharacter, indentationSize = get_Settings ( view )

	indentationQuery = re.search ( "[^\\s*]", text )

	if indentationQuery:
		indentationPosition = indentationQuery.start()
	else:
		indentationPosition = -1

	if indentationPosition == 0:
		return ( 0 )

	else:

		if indentationPosition == -1:
			indentation = text
		else:
			indentation = text[ 0 : indentationPosition ]

		spaceCount = indentation.count ( SPACE_CHARACTER )
		tabCount   = indentation.count ( TAB_CHARACTER )

		if  roundUp_Enabled \
		and spaceCount % 2 != 0:
			spaceCount += 1

		indentationLevel = int ( ( ( tabCount * indentationSize ) + spaceCount ) / indentationSize )
		return ( indentationLevel )
