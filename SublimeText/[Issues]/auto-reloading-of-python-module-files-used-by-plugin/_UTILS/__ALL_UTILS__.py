def load ( globalRef ):

	initialize_Globals ( utilsPath, globalRef )

def get_AllModules ( modulePath ):

	result = set()

	for module in os.listdir ( modulePath ):

		if os.path.isfile ( os.path.join ( modulePath, module ) ):
			pythonFile_Query = re.search ( rx_PythonFile, module )

			if pythonFile_Query:
				result.add ( pythonFile_Query.groups()[0] )

	return result

def initialize_Globals ( modulePath, globalRef ):

	sys.path.append ( modulePath )

	for moduleName in sorted ( get_AllModules ( modulePath ) ):

		if ( moduleName == allUtils_ModuleName ):
			continue
		else:
			globalRef[ moduleName ] = __import__ ( moduleName )
