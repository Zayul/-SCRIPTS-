def view_IsWidget ( view ):

	widget_IsFocused = view.settings().get ( "is_widget" )

	if widget_IsFocused:
		return ( True )
	else:
		return ( False )

def region_IsInverted ( region ):

	if region.a > region.b:
		region = sublime.Region ( region.b, region.a )

	return ( region )

def region_IsPoint ( region ):

	if region.a == region.b:
		return ( True )
	else:
		return ( False )
