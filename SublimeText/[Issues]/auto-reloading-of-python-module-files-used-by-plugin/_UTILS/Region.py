def get_SubRegions ( view, region ):

	region_A = region.a
	region_B = region.b

	line_A   = view.line ( region.a )
	line_A_A = line_A.a
	line_A_B = line_A.b

	line_B   = view.line ( region.b )
	line_B_A = line_B.a
	line_B_B = line_B.b

	lines = view.lines ( region )

	return ( region_A, region_B, line_A, line_A_A, line_A_B, line_B, line_B_A, line_B_B, lines )

def get_SubRegion_Text ( view, region ):

	region_A, region_B, line_A, line_A_A, line_A_B, line_B, line_B_A, line_B_B, lines = get_SubRegions ( view, region )

	regionText  = view.substr ( region )
	line_A_Text = view.substr ( line_A )
	line_B_Text = view.substr ( line_B )

	return ( regionText, line_A_Text, line_B_Text )

