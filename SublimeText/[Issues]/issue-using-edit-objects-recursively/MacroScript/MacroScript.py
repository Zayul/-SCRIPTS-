class MacroScriptCommand ( sublime_plugin.TextCommand ):
	def run \
		(
			self,
			edit,
			MODE          = "",
			COUNT         = 1,
			TEXT          = "",
			QUERY         = "",
			REGEX_ENABLED = False,
			IGNORE_CASE   = False,
		):

		#▓▓▓▓▓      • •   { Setup }      ▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓⌠¦••g1⌡#

		global V

		if ( V == None ):
			V = Variables()

		V.edit   = edit
		V.window = self.view.window()

		if V.view == None:
			V.view = self.view

		#▓▓▓▓▓      • •   { Select } Macro Tools Command      ▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓▓⌠¦••g1⌡#

		if MODE == "": return

		elif MODE == "initialize_MacroScript": Commands.initialize_MacroScript ()
		elif MODE == "edit_Copy":              Commands.edit_Copy              ()
		elif MODE == "edit_Paste":             Commands.edit_Paste             ( COUNT )
		elif MODE == "replace_All":            Commands.replace_All            ( QUERY, TEXT, REGEX_ENABLED, IGNORE_CASE )
		elif MODE == "find_Instance":          Commands.find_Instance          ( QUERY, COUNT, REGEX_ENABLED, IGNORE_CASE )
		elif MODE == "file_New":               Commands.file_New               ()
		elif MODE == "move_Line_Up":           Commands.move_Line_Up           ( COUNT )
		elif MODE == "move_Line_Down":         Commands.move_Line_Down         ( COUNT )
		elif MODE == "select_All":             Commands.select_All             ()
		elif MODE == "delete_Document_Start":  Commands.delete_Document_Start  ()
		elif MODE == "delete_Document_End":    Commands.delete_Document_End    ()

