class Commands ():

	def delete_Character_Left ( COUNT ):
		for index in range ( 0, COUNT ):
			V.view.run_command ( "left_delete" )

	def delete_Document_End ():
		Commands.select_Document_End ()
		Commands.delete_Character_Left ( 1 )

	def delete_Document_Start ():
		Commands.select_Document_Start ()
		Commands.delete_Character_Left ( 1 )

	def edit_Copy ():
		V.view.run_command ( "copy" )

	def edit_Paste ( COUNT ):
		for index in range ( 0, COUNT ):
			V.view.run_command ( "paste" )

	def file_New ():
		V.view = V.window.new_file()
		V.window.focus_view ( V.view )

	def find_Instance ( QUERY, COUNT, REGEX_ENABLED, IGNORE_CASE ):
		V.view.selection.clear ()
		queryRegions = Utilities.get_QueryRegions ( QUERY, REGEX_ENABLED, IGNORE_CASE )
		queryCount   = len ( queryRegions )
		queryIndex   = COUNT - 1

		if COUNT <= queryCount \
		and queryCount > 0:
			resultRegion = queryRegions [ queryIndex ]
			V.view.selection.add ( resultRegion )

	def initialize_MacroScript ():
		V.view = None

	def move_Line_Down ( COUNT ):
		for index in range ( 0, COUNT ):
			V.view.run_command \
				( "move",
					{ "by":      "lines",
						"forward": True,
						"extend":  False, } )

	def move_Line_Up ( COUNT ):
		for index in range ( 0, COUNT ):
			V.view.run_command \
				( "move",
					{ "by":      "lines",
						"forward": False,
						"extend":  False, } )

	def replace_All ( QUERY, TEXT, REGEX_ENABLED, IGNORE_CASE ):
		queryRegions = Utilities.get_QueryRegions ( QUERY, REGEX_ENABLED, IGNORE_CASE )
		queryCount   = len ( queryRegions )

		if queryCount > 0:
			for queryRegion in reversed ( queryRegions ):
				V.view.replace ( V.edit, queryRegion, TEXT )

	def select_All ():
		V.view.run_command ( "select_all" )

	def select_Document_End ():
		V.view.run_command \
			( "move_to",
					{ "to":     "eof",
						"extend": True, } )

	def select_Document_Start ():
		V.view.run_command \
			( "move_to",
					{ "to":     "bof",
						"extend": True, } )

